            # for z range
# Funkcja range zwraca sekwencję liczb, ktora domyślnie zaczyna sie od zera i inkrementuje do
# podanej wartości  minus 1, czyli range(5) utworzy sekwencję [0,1,2,3,4]

for v in range(3):          # [0,1,2]
    print(v)

for v in range(2, 5):       # [2,3,4]
    print(v)

for v in range(2, 8, 2):     # range (start, end, step)
    print(v)